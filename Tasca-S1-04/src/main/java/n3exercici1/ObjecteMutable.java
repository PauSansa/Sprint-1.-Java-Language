package n3exercici1;

public class ObjecteMutable {
    private int enter;
    private String text;
    private boolean boolea;

    public ObjecteMutable(int enter) {
        this.enter = enter;
    }

    public ObjecteMutable(String text) {
        this.text = text;
    }

    public ObjecteMutable(boolean boolea) {
        this.boolea = boolea;
    }
}
